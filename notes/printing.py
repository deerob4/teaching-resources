# By 'print', we just mean to write something out to the console.
# It can be used as a way to display data to users, or to debug things.
# Here, we are just printing out the words 'Hello world!'

print("Hello world!")

# But here, we are printing out a mathematical sum.
# This line will print out '4'.
# Note the lack of speech marks.

print(2 + 2)

# On the other hand, this will print out '2 + 2'.
# The speech marks make it a string.

print("2 + 2")